/// Supported store categories.
enum StoreType {
  /// Coffee shop or cafe.
  CAFE,

  /// Canteen or cafeteria.
  CANTEEN,

  /// Full-service or quick-service restaurant.
  RESTAURANT,

  /// Stationery and office supplies store.
  STATIONERY,

  /// Bakery shop.
  BAKERY,

  /// Grocery or convenience store.
  GROCERY,

  /// Any other store type not listed.
  OTHER,
}
